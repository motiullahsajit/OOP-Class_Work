public class Student {
    private String name;
    private int age;
    private int rollNumber;


    void setName(String name){
        this.name = name;
    }

    void setAge(int age){
        this.age = age;
    }

    void setRollNumber(int rollNumber){
        this.rollNumber = rollNumber;
    }

    String getName(){
        return name;
    }

    int getAge(){
        return age;
    }

    int getRollNumber(){
        return rollNumber;
    }

    
}
